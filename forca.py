import random

def jogar():

   mensagem_abertura()
   palavra_secreta = carrega_palavra_secreta()
   letras_acertadas = inicializa_letras_acertadas(palavra_secreta)

   enforcou = False
   acertou = False
   erros = 0

   print(letras_acertadas)

   while (not acertou and not enforcou):
        chute = chute_jogador()

        if(chute in letras_acertadas):
            print("Você já escolheu essa letra!")
        if (chute in palavra_secreta and chute not in letras_acertadas):
          chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros = erros + 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

   if(acertou):
     imprime_mensagem_ganhador()
   else:
       imprime_mensagem_perdedor(palavra_secreta)

   print("Fim de jogo!")


def mensagem_abertura():
    print("********************************")
    print("Bem vindo ao Jogo da Forca das Frutas!!")
    print("********************************")


def carrega_palavra_secreta():
    palavras = []
    with open("palavras.txt", "r") as arquivo:
        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta

def inicializa_letras_acertadas(palavra):
    return ["_" for letra in palavra]


def chute_jogador():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()
    return chute


def chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute.upper() == letra.upper()):
            letras_acertadas[index] = letra
        index = index + 1

